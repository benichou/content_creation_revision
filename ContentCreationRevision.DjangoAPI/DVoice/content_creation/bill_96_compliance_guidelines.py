
def get_bill_96_compliance_guideline() -> str:
    """
    Retrieves the compliance guidelines for Bill 96, which mandates bilingual content 
    (English and French) with identical page content in both languages.

    Args:
        None
 
    Returns:
        bill_96_compliance (str): A string containing the compliance guidelines for Bill 96.
    """
    bill_96_compliance_guideline = """
                                   - The bill requires that the content has both English \
                                   and French content.
                                   - EN and FR page content has to be identical.
                                   """
    return bill_96_compliance_guideline